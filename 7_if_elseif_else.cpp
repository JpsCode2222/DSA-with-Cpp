#include <iostream>
using namespace std;
int main()
{
    // num = positive , negative , zero
    int num = 0;
    if (num > 0)
    {
        cout << "Number is Positive";
    }
    else if (num < 0)
    {
        cout << "Number is Negative";
    }
    else
    {
        cout << "Number is Zero";
    }
    return 0;
}